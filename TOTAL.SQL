DECLARE 
	SNAME RESULT.NAME%TYPE;
	S1 RESULT.SUB1%TYPE;
	S2 RESULT.SUB2%TYPE;
	S3 RESULT.SUB3%TYPE;
	S4 RESULT.SUB4%TYPE;
	S5 RESULT.SUB5%TYPE;
	
	
	I NUMBER(1);
	
	
	TOT NUMBER;
	PERS NUMBER;
	GRD VARCHAR2(1);
	
BEGIN
	<<C_LOOP>>
	FOR I IN 1..3 LOOP
	SELECT NAME,SUB1,SUB2,SUB3,SUB4,SUB5 INTO SNAME,S1,S2,S3,S4,S5 FROM RESULT WHERE ROLLNO=I;
	TOT:=S1+S2+S3+S4+S5;
	PERS := (100*TOT)/500;
	
	
	IF (PERS>90 AND PERS<100) THEN
		DBMS_OUTPUT.PUT_LINE('A GRADE');
		DBMS_OUTPUT.PUT_LINE('TOTAL OF ALL SUBJECT IS : '||TOT);
		DBMS_OUTPUT.PUT_LINE(PERS);
		GRD:='A';
	ELSIF (PERS>80 AND PERS<90) THEN
		DBMS_OUTPUT.PUT_LINE('B GRADE');
		DBMS_OUTPUT.PUT_LINE('TOTAL OF ALL SUBJECT IS : '||TOT);
		DBMS_OUTPUT.PUT_LINE(PERS);
		GRD:='B';
	ELSIF (PERS>70 AND PERS<80) THEN
		DBMS_OUTPUT.PUT_LINE('C GRADE');
		DBMS_OUTPUT.PUT_LINE('TOTAL OF ALL SUBJECT IS : '||TOT);
		DBMS_OUTPUT.PUT_LINE(PERS);
		GRD:='C';
	ELSIF (PERS>60 AND PERS<70) THEN
		DBMS_OUTPUT.PUT_LINE('D GRADE');
		DBMS_OUTPUT.PUT_LINE('TOTAL OF ALL SUBJECT IS : '||TOT);
		DBMS_OUTPUT.PUT_LINE(PERS);
		GRD:='D';
	ELSIF (PERS>33 AND PERS<60) THEN
		DBMS_OUTPUT.PUT_LINE('E GRADE');
		DBMS_OUTPUT.PUT_LINE('TOTAL OF ALL SUBJECT IS : '||TOT);
		DBMS_OUTPUT.PUT_LINE(PERS);
		GRD:='E';
	ELSE
		DBMS_OUTPUT.PUT_LINE('F GRADE');
		DBMS_OUTPUT.PUT_LINE('TOTAL OF ALL SUBJECT IS : '||TOT);
		DBMS_OUTPUT.PUT_LINE(PERS);
		GRD:='F';
	END IF;
	
	UPDATE RESULT SET GRADE = GRD WHERE ROLLNO=I;
	
	END LOOP C_LOOP;
END;
